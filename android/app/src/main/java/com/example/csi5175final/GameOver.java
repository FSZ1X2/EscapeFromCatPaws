    /**
     * function for getting the bitmap for sharing.
     *
     * @return bitmap generated by the app.
     */
    private void makeShareImage(){
        //get template from resource
        Drawable shareTmp = getDrawable(R.drawable.share);
        Bitmap shareImg = ((BitmapDrawable) shareTmp).getBitmap();
        android.graphics.Bitmap.Config bitmapConfig = shareImg.getConfig();
        // set default bitmap config if none
        if(bitmapConfig == null) {
                bitmapConfig = android.graphics.Bitmap.Config.ARGB_8888;
        }
        // convert resource bitmaps are to mutable one
        shareImg = shareImg.copy(bitmapConfig, true);
        Canvas canvas = new Canvas(shareImg);
        //set up paint brush
        Paint paint = new Paint(Paint.ANTI_ALIAS_FLAG);
        paint.setColor(Color.BLACK); // Text Color
        paint.setTextSize(100); // Text Size
        // draw text to the bitmap
        canvas.drawText("Your score is:", 250, 1350, paint);
        canvas.drawText(current_score, 500, 1450, paint);
        // save bitmap to cache directory
        try {

            File cachePath = new File(GameOver.this.getCacheDir(), "images");
            cachePath.mkdirs(); // don't forget to make the directory
            FileOutputStream stream = new FileOutputStream(new File(cachePath, "image.png")); // overwrites this image every time
            shareImg.compress(Bitmap.CompressFormat.PNG, 100, stream);
            stream.close();

        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    /**
     * function for sharing the PNG image from Uri.
     */
    private void shareImage(){
        File imagePath = new File(GameOver.this.getCacheDir(), "images");
        File newFile = new File(imagePath, "image.png");
        Uri contentUri = FileProvider.getUriForFile(GameOver.this, "com.example.csi5175final.provider", newFile);

        if (contentUri != null) {
            Intent intent = new Intent(android.content.Intent.ACTION_SEND);
            intent.putExtra(Intent.EXTRA_STREAM, contentUri);
            intent.addFlags(Intent.FLAG_GRANT_READ_URI_PERMISSION);
            intent.setType("image/png");
            startActivity(intent);
        }
    }

